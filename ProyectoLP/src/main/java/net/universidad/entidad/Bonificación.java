package net.universidad.entidad;

public class Bonificación {

	private int codSolBon,codTrabajador;
	private String detalle,estado,nomTrabajador,fecha;
	public int getCodSolBon() {
		return codSolBon;
	}
	public void setCodSolBon(int codSolBon) {
		this.codSolBon = codSolBon;
	}
	public int getCodTrabajador() {
		return codTrabajador;
	}
	public void setCodTrabajador(int codTrabajador) {
		this.codTrabajador = codTrabajador;
	}
	public String getDetalle() {
		return detalle;
	}
	public void setDetalle(String detalle) {
		this.detalle = detalle;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getNomTrabajador() {
		return nomTrabajador;
	}
	public void setNomTrabajador(String nomTrabajador) {
		this.nomTrabajador = nomTrabajador;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	
	
	
	
	
}
